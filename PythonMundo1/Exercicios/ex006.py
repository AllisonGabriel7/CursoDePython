nd = 2*n
nt = 3*n
nr = n**(1/2)
print("O dobro desse número é: ")
time.sleep(1)
print(f"{nd}")
time.sleep(1)
print("O triplo desse número é: ")
time.sleep(1)
print(f"{nt}")
time.sleep(1)
print("A raiz quadrada desse número é: ")
time.sleep(1)
print(f"{nr}")
